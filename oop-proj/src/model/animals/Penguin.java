package model.animals;

public class Penguin extends Bird{
	private String slide_on_belly;
	private String swimming;
	public String getSlide_on_belly() {
		return slide_on_belly;
	}
	public void setSlide_on_belly(String slide_on_belly) {
		this.slide_on_belly = slide_on_belly;
	}
	public String getSwimming() {
		return swimming;
	}
	public void setSwimming(String swimming) {
		this.swimming = swimming;
	}
}
